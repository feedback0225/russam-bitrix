            <div class="full-text about__desc">
                <p>В 1912 году по указу Николая II было основано общество «Русские самоцветы», которое объединило крупнейшие ювелирные фирмы того времени, в том числе мастерские Карла Фаберже.</p>
                <p>Более ста лет «Русские самоцветы» сохраняют технологии знаменитой петербургской ювелирной школы и разрабатывают инновационные методы изготовления драгоценных шедевров. Мастера создают роскошные украшения, столовое серебро, эмалево-филигранные изделия и предметы интерьера, которые отличают оригинальность авторских идей и непревзойденный уровень исполнения. </p>
                <p>Императорский ювелирный дом «Русские самоцветы» неоднократно завоевывал престижные награды от ведущих экспертов отрасли, что подтверждает качество и высокую художественную ценность изделий. </p>
